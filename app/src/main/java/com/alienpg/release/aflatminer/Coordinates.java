public class Coordinates {

    int x;
    int y;

    public Coordinates()
    {

    }

    public void setCoordinates(int newX,int newY)
    {
        x = newX;
        y = newY;
    }

    int getX()
    {
        return x;
    }

    int getY()
    {
        return y;
    }


}
